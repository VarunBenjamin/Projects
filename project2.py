#Assigning elements to different lists
listt=['Alice','Bob','Charlie']
print('My list is: ',listt)


#Accessing elements from a tuple
tup=('Alice','Bob','Charlie')
print('My tuple is: ',tup)
print('Tuple at index 1 is: ',tup[1])


#Deleting different dictionary elements
dic={'Alice':21,'Bob':22,'Charlie':21}
print('My dictionary is:',dic)
del dic['Alice']
print('The dictionary after deleting a name is: ',dic)
